package com.tourtravel.enums;

/**
 * Status of Contact Us enquiry.
 */
public enum ContactStatus {

    /**
     * New enquiry received.
     */
    NEW,

    /**
     * Admin has viewed the enquiry.
     */
    READ,

    /**
     * Admin has replied to the enquiry.
     */
    RESPONDED,

    /**
     * Enquiry has been closed.
     */
    CLOSED

}